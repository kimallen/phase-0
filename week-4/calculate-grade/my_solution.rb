def get_grade(avg)
  if avg >= 90
    puts "A"
    return 'A'
  elsif avg >= 80
    puts "A"
    return 'B'
  elsif avg >= 70
    puts "A"
    return 'C'
  elsif avg >= 60
    puts "A"
    return 'D'
  elsif avg < 60
    puts "A"
    return 'F'
  end
end